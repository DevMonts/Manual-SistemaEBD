            <h1 class="text-3xl font-bold text-orange-600 mb-4">Entendendo as Permissões de Usuário no Sistema EBD</h1>
            <p class="mb-4">
                O Sistema EBD (Escola Bíblica Dominical) foi desenvolvido para permitir a atribuição de diferentes níveis de permissões de acesso aos usuários, conforme o cargo ou função que desempenham na organização. Abaixo, apresentamos as permissões de acesso para cada perfil de usuário, detalhando suas responsabilidades e limitações.
            </p>
            <hr class="my-6" />

            <h2 class="text-2xl font-semibold text-orange-500 mb-3">Perfis e Permissões</h2>
            <ul class="list-disc list-inside space-y-2 mb-6">
                <li><strong>Administrador(a):</strong>
                    <span class="block">Permissões: O administrador possui permissões completas dentro do Sistema EBD. Isso inclui o gerenciamento de aulas, turmas, usuários, relatórios e mais. Esse perfil tem controle total sobre o sistema, podendo adicionar ou editar qualquer dado, configurar o sistema de acordo com as necessidades da escola e gerar relatórios de diversos aspectos da gestão educacional.</span>
                    <span class="block">Responsabilidade: Gestão completa do sistema e suporte para a administração de todas as funcionalidades.</span>
                </li>
                <li><strong>Coordenador(a):</strong>
                    <span class="block">Permissões: O coordenador tem acesso amplo, com permissão para visualizar e editar informações relacionadas a aulas, turmas, relatórios, entre outros. Sua função envolve a supervisão geral, podendo tomar decisões informadas com base nos dados disponíveis.</span>
                    <span class="block">Responsabilidade: Acompanhamento e moderação das aulas e turmas, além da geração de relatórios e apoio à gestão administrativa.</span>
                </li>
                <li><strong>Secretário(a):</strong>
                    <span class="block">Permissões: O secretário tem acesso para gerenciar aulas, turmas e usuários com foco na administração do sistema. Sua atuação é voltada para o controle organizacional e apoio administrativo, podendo editar e atualizar registros necessários para o funcionamento das aulas.</span>
                    <span class="block">Responsabilidade: Responsável por registrar e atualizar informações, garantindo que todas as turmas e usuários estejam devidamente cadastrados e atualizados.</span>
                </li>
                <li><strong>Secretário(a) de Turma:</strong>
                    <span class="block">Permissões: Este perfil tem acesso restrito apenas à sua própria turma. O secretário de turma pode gerenciar os alunos dessa turma, incluindo a edição de relatórios específicos relacionados a ela. A atuação é mais focada nas necessidades imediatas da turma que lhe foi atribuída.</span>
                    <span class="block">Responsabilidade: Gerenciamento de informações relacionadas à turma específica, como registros de alunos, relatórios de presença e outras funções administrativas.</span>
                </li>
                <li><strong>Professor(a):</strong>
                    <span class="block">Permissões: O professor tem acesso às informações da turma em que leciona, podendo visualizar e editar dados relacionados aos alunos e à gestão das aulas. A principal diferença entre os professores está no cadastro como aluno:</span>
                    <ul class="list-disc list-inside">
                        <li><strong>Professor(a) com Cadastro de Aluno:</strong> Este perfil também está cadastrado como aluno no sistema, o que permite sua inclusão nas chamadas.</li>
                        <li><strong>Professor(a) sem Cadastro de Aluno:</strong> Este perfil não é cadastrado como aluno, portanto, não aparece na chamada.</li>
                    </ul>
                    <span class="block">Responsabilidade: Ministrar aulas e acompanhar o desempenho dos alunos. O professor pode ter diferentes níveis de acesso dependendo de sua inclusão ou não no cadastro de aluno, mas em ambos os casos sua principal função é o ensino e a condução das atividades pedagógicas.</span>
                </li>
                <li><strong>Aluno:</strong> Atualmente, não possui acesso ao sistema.</li>
            </ul>

            <hr class="my-6" />
