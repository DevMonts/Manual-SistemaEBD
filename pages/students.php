            <p class="mb-6">
                Para registrar a chamada de uma aula, siga os passos abaixo:
            </p>
            <ol class="list-decimal list-inside space-y-2 mb-6">
                <li>Acesse o menu <strong>Lições</strong> no sistema.</li>
                <li>Escolha a lição que deseja registrar a presença.</li>
                <li>Selecione a <strong>turma</strong> desejada.</li>
                <li>A tela de chamada será exibida com os alunos daquela turma.</li>
                <li>Marque os presentes e registre a oferta da aula, se houver.</li>
                <li>Clique em <strong>Salvar</strong> para concluir o registro da chamada.</li>
            </ol>
            <hr class="my-6">
            <h2 class="text-2xl font-semibold text-orange-500 mb-3">Funções disponíveis na tela de alunos</h2>
            <ul class="list-disc list-inside space-y-2 mb-6">
                <li><strong>Cadastrar aluno</strong>: Permite adicionar um novo aluno informando nome, data de nascimento, turma e status.</li>
                <li><strong>Editar aluno</strong>: Atualiza os dados de um aluno já cadastrado.</li>
                <li><strong>Transferir aluno</strong>: Move um aluno de uma turma para outra.</li>
                <li><strong>Excluir aluno</strong>: Remove o aluno do sistema (ação permanente).</li>
                <li><strong>Filtrar e buscar</strong>: Localize alunos pelo nome, turma ou status usando a barra de pesquisa.</li>
            </ul>
