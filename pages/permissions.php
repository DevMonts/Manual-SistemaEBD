            <p class="mb-4">
                <strong>Múltiplos Níveis de Acesso:</strong> Um usuário pode possuir múltiplos níveis de acesso, dependendo das necessidades e da estrutura administrativa da escola. Isso permite que as permissões sejam ajustadas de acordo com a evolução das funções e responsabilidades dentro da organização.
            </p>

            <hr class="my-6" />
            <h2 class="text-2xl font-semibold text-orange-500 mb-3">Importante</h2>
            <p class="mb-4">
                <strong>Eficiência na Gestão de Permissões:</strong> Atribuir corretamente as permissões aos usuários é fundamental para garantir a segurança e a eficácia na gestão da Escola Bíblica Dominical. É importante que todos os usuários conheçam bem as permissões que têm, para que possam usar o Sistema EBD de maneira eficaz. Ao conhecer as permissões de cada perfil, a escola pode otimizar seus processos e promover um ambiente mais seguro e organizado, possibilitando o melhor aproveitamento do sistema.
            </p>

